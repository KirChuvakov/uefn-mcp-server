"""Generate markdown API documentation from a .pyi type stub.

Usage:
    python tools/generate_api_docs.py --stub docs/uefn_stub.pyi
    python tools/generate_api_docs.py --stub docs/uefn_stub.pyi --output docs/generated

Parses the .pyi stub generated by generate_uefn_stub.py and produces
organized markdown documentation files in docs/generated/.
"""

import argparse
import os
import re
import sys
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple


# ---------------------------------------------------------------------------
# Data model
# ---------------------------------------------------------------------------


@dataclass
class Member:
    name: str
    kind: str  # "property", "method", "enum_value"
    signature: str = ""
    return_type: str = ""
    description: str = ""
    value: str = ""  # for enum members


@dataclass
class TypeInfo:
    name: str
    kind: str  # "class", "struct", "enum", "function"
    base: str = ""
    docstring: str = ""
    members: List[Member] = field(default_factory=list)
    module: str = ""  # C++ source module if available


# ---------------------------------------------------------------------------
# Parser
# ---------------------------------------------------------------------------


def parse_stub(path: str) -> Tuple[List[TypeInfo], Dict[str, str]]:
    """Parse a .pyi stub file into a list of TypeInfo objects.

    Returns (types, metadata) where metadata has header stats.
    """
    with open(path, "r", encoding="utf-8") as f:
        content = f.read()

    lines = content.split("\n")
    types: List[TypeInfo] = []
    metadata: Dict[str, str] = {}
    current_section = ""

    # Parse header comments for metadata
    for line in lines[:10]:
        if line.startswith("# Types:"):
            metadata["types_summary"] = line[2:].strip()
        elif line.startswith("# Auto-generated"):
            metadata["source"] = line[2:].strip()

    i = 0
    while i < len(lines):
        line = lines[i]

        # Track section headers
        if line.startswith("# ==="):
            if i + 1 < len(lines):
                section_line = lines[i + 1].strip("# ").strip()
                if section_line.startswith("Top-level"):
                    current_section = "function"
                elif section_line.startswith("Enum"):
                    current_section = "enum"
                elif section_line.startswith("Struct"):
                    current_section = "struct"
                elif section_line.startswith("Class"):
                    current_section = "class"
            i += 1
            continue

        # Top-level function
        if current_section == "function" and line.startswith("def "):
            info = _parse_function(line)
            if info:
                types.append(info)
            i += 1
            continue

        # Class/struct/enum definition
        if line.startswith("class "):
            info, i = _parse_class(lines, i, current_section)
            if info:
                types.append(info)
            continue

        i += 1

    return types, metadata


def _parse_function(line: str) -> Optional[TypeInfo]:
    """Parse a top-level function line like: def name(args) -> Type: ...  # desc"""
    m = re.match(r"def (\w+)(\(.*?\))(?:\s*->.*?)?: \.\.\.(?:\s*#\s*(.*))?", line)
    if not m:
        # Try simpler pattern
        m = re.match(r"def (\w+)(\(.*)", line)
        if not m:
            return None
        name, sig_rest = m.group(1), m.group(2)
        desc = ""
        if "#" in sig_rest:
            sig_rest, desc = sig_rest.rsplit("#", 1)
            desc = desc.strip()
        sig = sig_rest.split(": ...")[0].strip()
        return TypeInfo(
            name=name, kind="function",
            docstring=desc,
            members=[Member(name=name, kind="method", signature=sig, description=desc)],
        )

    name, sig, desc = m.group(1), m.group(2), m.group(3) or ""
    return TypeInfo(
        name=name, kind="function",
        docstring=desc,
        members=[Member(name=name, kind="method", signature=sig, description=desc)],
    )


def _parse_class(lines: List[str], start: int, section: str) -> Tuple[Optional[TypeInfo], int]:
    """Parse a class definition starting at the given line index."""
    line = lines[start]
    m = re.match(r"class (\w+)(?:\((\w+)\))?:", line)
    if not m:
        return None, start + 1

    name = m.group(1)
    base = m.group(2) or ""
    kind = section if section in ("enum", "struct", "class") else "class"

    info = TypeInfo(name=name, kind=kind, base=base)

    i = start + 1
    # Parse class body (indented lines)
    in_docstring = False
    docstring_lines: List[str] = []
    pending_property = False

    while i < len(lines):
        raw = lines[i]
        # Join continuation lines (e.g. "def foo() -> Type\n: ...")
        if not in_docstring and raw.startswith("    ") and not raw.strip().startswith(('"""', 'r"""')):
            while (i + 1 < len(lines)
                   and not raw.rstrip().endswith(": ...")
                   and not raw.rstrip().endswith('"""')
                   and not raw.strip() == "..."
                   and not raw.strip() == ""
                   and not raw.strip() == "@property"
                   and lines[i + 1].startswith("    ")
                   and not lines[i + 1].strip().startswith(("def ", "class ", "@", '"""', 'r"""'))):
                i += 1
                raw = raw.rstrip() + lines[i].strip()
            # Also join if next line is just ": ..." or ": ...  # comment"
            if (i + 1 < len(lines)
                    and re.match(r"^: \.\.\.(\s*#.*)?$", lines[i + 1].strip())):
                i += 1
                raw = raw.rstrip() + lines[i].strip()
        if not raw.startswith("    ") and raw.strip() != "" and not in_docstring:
            break
        if raw.strip() == "":
            if not in_docstring:
                i += 1
                # An empty line after unindented content means end of class
                if i < len(lines) and not lines[i].startswith("    "):
                    break
                continue
            else:
                docstring_lines.append("")
                i += 1
                continue

        stripped = raw.strip()

        # Docstring body/close must be checked BEFORE opener detection,
        # otherwise `"""` on its own is mistaken for a new docstring start.
        if in_docstring:
            if '"""' in stripped:
                before = stripped.split('"""')[0]
                if before:
                    docstring_lines.append(before)
                info.docstring = "\n".join(docstring_lines).strip()
                _extract_module(info, info.docstring)
                docstring_lines = []
                in_docstring = False
                i += 1
                continue
            else:
                docstring_lines.append(stripped)
                i += 1
                continue

        # Docstring opener (only when not already inside one)
        if stripped.startswith('r"""') or stripped.startswith('"""'):
            prefix = 'r"""' if stripped.startswith('r"""') else '"""'
            rest = stripped[len(prefix):]
            if rest.endswith('"""') and len(rest) > 3:
                # Single-line docstring
                info.docstring = rest[:-3].strip()
                _extract_module(info, info.docstring)
                i += 1
                continue
            elif rest.endswith('"""'):
                info.docstring = rest[:-3].strip()
                _extract_module(info, info.docstring)
                i += 1
                continue
            else:
                in_docstring = True
                if rest:
                    docstring_lines.append(rest)
                i += 1
                continue

        # @property decorator
        if stripped == "@property":
            pending_property = True
            i += 1
            continue

        # Property definition
        if pending_property and stripped.startswith("def "):
            pending_property = False
            pm = re.match(
                r"def (\w+)\(self\)\s*->\s*([\w\[\], ]+):\s*\.\.\.(?:\s*#\s*(.*))?",
                stripped,
            )
            if pm:
                info.members.append(Member(
                    name=pm.group(1),
                    kind="property",
                    return_type=pm.group(2).strip(),
                    description=pm.group(3).strip() if pm.group(3) else "",
                ))
            i += 1
            continue

        # Method definition
        if stripped.startswith("def "):
            mm = re.match(
                r"def (\w+)(\(.*?\))(?:\s*->.*?)?\s*:\s*\.\.\.(?:\s*#\s*(.*))?",
                stripped,
            )
            if mm:
                info.members.append(Member(
                    name=mm.group(1),
                    kind="method",
                    signature=mm.group(2),
                    description=mm.group(3).strip() if mm.group(3) else "",
                ))
            i += 1
            continue

        # Enum member: NAME: ClassName = ...  # value: desc
        if kind == "enum" and ": " in stripped and "= ..." in stripped:
            em = re.match(
                r"(\w+):\s*\w+\s*=\s*\.\.\.(?:\s*#\s*(.*))?",
                stripped,
            )
            if em:
                val_desc = em.group(2) or ""
                val = ""
                desc = val_desc
                if ":" in val_desc:
                    val, desc = val_desc.split(":", 1)
                    val = val.strip()
                    desc = desc.strip()
                info.members.append(Member(
                    name=em.group(1),
                    kind="enum_value",
                    value=val,
                    description=desc,
                ))
            i += 1
            continue

        # Ellipsis placeholder for empty classes
        if stripped == "...":
            i += 1
            continue

        i += 1

    return info, i


def _extract_module(info: TypeInfo, docstring: str) -> None:
    """Extract C++ module name from docstring."""
    m = re.search(r"\*\*Module:\*\*\s*(\w+)", docstring)
    if m:
        info.module = m.group(1)
    else:
        m = re.search(r"- \*\*Module\*\*\s*\S+\s*(\w+)", docstring)
        if m:
            info.module = m.group(1)


