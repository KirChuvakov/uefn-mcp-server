def generate_docs(types: List[TypeInfo], metadata: Dict[str, str], output_dir: str) -> None:
    """Generate markdown documentation files from parsed types."""
    os.makedirs(output_dir, exist_ok=True)

    # Classify types into domains
    domains: Dict[str, List[TypeInfo]] = {}
    functions: List[TypeInfo] = []

    for t in types:
        if t.kind == "function":
            functions.append(t)
            continue
        domain = classify_type(t)
        domains.setdefault(domain, [])
        domains[domain].append(t)

    # Sort domains by type count descending, but keep "Other" last
    sorted_domains = sorted(
        domains.items(),
        key=lambda x: (x[0] == "Other", -len(x[1])),
    )

    # Stats
    total_types = len(types)
    total_classes = sum(1 for t in types if t.kind == "class")
    total_enums = sum(1 for t in types if t.kind == "enum")
    total_structs = sum(1 for t in types if t.kind == "struct")
    total_functions = len(functions)

    # --- Generate README.md (index) ---
    readme_lines = [
        "# UEFN Python API Documentation",
        "",
        "> Auto-generated from `.pyi` stub via `tools/generate_api_docs.py`",
        "",
        f"**{total_types:,} types** — "
        f"{total_classes:,} classes, {total_enums:,} enums, "
        f"{total_structs:,} structs, {total_functions:,} functions",
        "",
        "---",
        "",
        "## Domains",
        "",
        "| Domain | Classes | Enums | Structs | Total | File |",
        "|--------|---------|-------|---------|-------|------|",
    ]

    for domain, dtypes in sorted_domains:
        fname = _domain_filename(domain)
        nc = sum(1 for t in dtypes if t.kind == "class")
        ne = sum(1 for t in dtypes if t.kind == "enum")
        ns = sum(1 for t in dtypes if t.kind == "struct")
        readme_lines.append(
            f"| {domain} | {nc} | {ne} | {ns} | {len(dtypes)} | [{fname}]({fname}) |"
        )

    if functions:
        readme_lines.append(
            f"| Top-level Functions | — | — | — | {len(functions)} | "
            f"[functions.md](functions.md) |"
        )

    readme_lines.extend(["", "---", ""])

    _write_file(os.path.join(output_dir, "README.md"), "\n".join(readme_lines))

    # --- Generate per-domain files ---
    for domain, dtypes in sorted_domains:
        fname = _domain_filename(domain)
        content = _generate_domain_doc(domain, dtypes)
        _write_file(os.path.join(output_dir, fname), content)

    # --- Generate functions file ---
    if functions:
        content = _generate_functions_doc(functions)
        _write_file(os.path.join(output_dir, "functions.md"), content)

    print(f"Generated {len(sorted_domains) + 2} files in {output_dir}/")
    print(f"  README.md (index)")
    print(f"  {len(sorted_domains)} domain files")
    if functions:
        print(f"  functions.md ({len(functions)} functions)")


def _domain_filename(domain: str) -> str:
    """Convert domain name to a safe filename."""
    name = domain.lower()
    name = re.sub(r"[^a-z0-9]+", "_", name)
    name = name.strip("_")
    return f"{name}.md"


def _generate_domain_doc(domain: str, types: List[TypeInfo]) -> str:
    """Generate markdown for a single domain."""
    lines = [
        f"# {domain}",
        "",
        f"> {len(types)} types",
        "",
    ]

    # Separate by kind
    enums = [t for t in types if t.kind == "enum"]
    structs = [t for t in types if t.kind == "struct"]
    classes = [t for t in types if t.kind == "class"]

    # Summary table
    top_classes = sorted(classes, key=lambda t: len(t.members), reverse=True)[:10]
    if top_classes:
        lines.append("**Key entry points:**")
        entries = [f"`{t.name}` ({len(t.members)} members)" for t in top_classes]
        lines.append(", ".join(entries))
        lines.extend(["", "---", ""])

    # Classes
    if classes:
        lines.append(f"## Classes ({len(classes)})")
        lines.append("")
        for t in sorted(classes, key=lambda x: x.name):
            lines.extend(_format_type(t))

    # Structs
    if structs:
        lines.append(f"## Structs ({len(structs)})")
        lines.append("")
        for t in sorted(structs, key=lambda x: x.name):
            lines.extend(_format_type(t))

    # Enums
    if enums:
        lines.append(f"## Enums ({len(enums)})")
        lines.append("")
        for t in sorted(enums, key=lambda x: x.name):
            lines.extend(_format_type(t))

    return "\n".join(lines)


def _format_type(t: TypeInfo) -> List[str]:
    """Format a single type as markdown."""
    lines = []
    header = f"### `{t.name}`"
    if t.base:
        header += f" (extends `{t.base}`)"
    lines.append(header)
    lines.append("")

    if t.docstring:
        # Clean up docstring for markdown
        doc = t.docstring.replace("r\"\"\"", "").replace('"""', "").strip()
        if doc:
            lines.append(f"> {doc}")
            lines.append("")

    properties = [m for m in t.members if m.kind == "property"]
    methods = [m for m in t.members if m.kind == "method"]
    enum_values = [m for m in t.members if m.kind == "enum_value"]

    if enum_values:
        lines.append("**Values:**")
        lines.append("")
        lines.append("| Name | Value | Description |")
        lines.append("|------|-------|-------------|")
        for m in enum_values:
            lines.append(f"| `{m.name}` | {m.value} | {m.description} |")
        lines.append("")

    if properties:
        lines.append("**Properties:**")
        lines.append("")
        lines.append("| Name | Type | Description |")
        lines.append("|------|------|-------------|")
        for m in properties:
            lines.append(f"| `{m.name}` | `{m.return_type}` | {m.description} |")
        lines.append("")

    if methods:
        lines.append("**Methods:**")
        lines.append("")
        for m in methods:
            desc = f" — {m.description}" if m.description else ""
            lines.append(f"- `{m.name}{m.signature}`{desc}")
        lines.append("")

    if not enum_values and not properties and not methods:
        lines.append("*No documented members.*")
        lines.append("")

    lines.append("---")
    lines.append("")
    return lines


def _generate_functions_doc(functions: List[TypeInfo]) -> str:
    """Generate markdown for top-level functions."""
    lines = [
        "# Top-level Functions",
        "",
        f"> {len(functions)} functions available on the `unreal` module",
        "",
        "| Function | Description |",
        "|----------|-------------|",
    ]

    for f in sorted(functions, key=lambda x: x.name):
        member = f.members[0] if f.members else None
        if member:
            sig = f"`{f.name}{member.signature}`"
            desc = member.description or ""
        else:
            sig = f"`{f.name}()`"
            desc = ""
        lines.append(f"| {sig} | {desc} |")

    lines.append("")
    return "\n".join(lines)


def _write_file(path: str, content: str) -> None:
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate markdown API docs from a UEFN .pyi stub"
    )
    parser.add_argument(
        "--stub", required=True,
        help="Path to the .pyi stub file (generated by generate_uefn_stub.py)",
    )
    parser.add_argument(
        "--output", default="docs/generated",
        help="Output directory for generated docs (default: docs/generated)",
    )
    args = parser.parse_args()

    if not os.path.isfile(args.stub):
        print(f"Error: stub file not found: {args.stub}", file=sys.stderr)
        sys.exit(1)

    print(f"Parsing stub: {args.stub}")
    types, metadata = parse_stub(args.stub)
    print(f"Parsed {len(types)} types")

    if not types:
        print("Warning: no types found in stub file. Is it a valid .pyi?", file=sys.stderr)
        sys.exit(1)

    generate_docs(types, metadata, args.output)
    print("Done.")


if __name__ == "__main__":
    main()
