def classify_type(info: TypeInfo) -> str:
    """Assign a domain category to a type based on name and module."""
    name = info.name

    # Check module first
    if info.module:
        mod = info.module
        if "Niagara" in mod:
            return "Niagara VFX"
        if "PCG" in mod:
            return "PCG (Procedural Content Generation)"
        if "GeometryScript" in mod:
            return "Geometry Scripting"

    # Check name prefix rules
    for prefix, domain in DOMAIN_RULES:
        if name.startswith(prefix):
            return domain

    # Fallback heuristics
    if info.base:
        for prefix, domain in DOMAIN_RULES:
            if info.base.startswith(prefix):
                return domain

    return "Other"


